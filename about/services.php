                    <h3>Why you should plan for retirement?</h3>
                    <p>
                     Good news! People on average are living longer and are able to remain healthy and active well into their sunset years.

                    <br> <br> But many individuals haven’t saved or invested enough money to retire in their 60s with the confidence that their funds will last for their lifetime. Both the Center for Retirement Research at Boston College and the Consumer Financial Protection Bureau have estimated that approximately 50% of today's retirees have cut back on their spending and will be forced to do so, due to dwindling resources.

                    <br> <br> Far too many retirees end up relying on Social Security to cover the majority of their living expenses only to find out the hard way that it isn't nearly enough. Social Security retirement income is only designed to replace about 40% of the average worker's salary, but more than one in five married couples and 45% of single retirees depend on Social Security for more than 90% of their incomes in retirement.

                    <br> <br> The bottom line is that, while many get by without ever making and executing a retirement plan, those who most enjoy their retirement do so in part due to having a retirement plan. Retirement planning is what can help you to be financially comfortable after you leave your job.

                    <br> <br> <b>What we consider when planning your retirement.</b>

                    <br> <br> When you want to retire? Are you planning to work until age 65 or until you are older than that? Do you have a goal of retiring early? How many more years, are you planning to spend in the workforce significantly affects how much money are you likely to need. If you choose to work until you are older, not only do your investments have more time to grow but the number of retirement years you need to fund is slightly reduced.

                    <br> <br> Where you want to live? Are you going to stay in your current home or downsize? Do you want to stay in the same area or retire somewhere warm or closer to relatives? The cost of living in the area where you'd like to live as a senior citizen is another major factor impacting how much money you will need in retirement.

                    How will you pay your living expenses? Your Social Security retirement income can't cover all of your expenses, so will you need to save or invest money as well? Yes, Another factor to consider is the magnitude of your living expenses themselves. Whether you own or rent property in retirement can significantly change the amount of your living expenses.
                    </p>

                    <h3>How much money you will need to retire?</h3>
                    <p>
                     You may be wondering what dollar amount will be enough money to comfortably retire. Unfortunately, there's not a one-size-fits-all number. To estimate how much money you personally need to retire, follow these basic steps:

                        <br> <br> 1. Estimate your total annual living expenses in retirement. You can use the rule of thumb that the typical you retiree needs about 80% of his or her pre-retirement income to maintain the same standard of living after leaving the workforce for good.

                        <br> <br> 2. Subtract your expected Social Security benefits and any pension income you expect to receive from your estimated total annual living expenses in retirement to compute your estimated net annual living expenses. Your latest Social Security statement, which you can find on the Social Security website, has an estimate of the Social Security income you are likely to receive.

                        <br> <br> 3. Multiply your estimated net annual living expenses in retirement by 25 to determine a total amount of money you need to save for retirement. Multiplying your expenses in retirement by 25 to determine the total amount of retirement money you need is linked to another rule of thumb called the 4% rule. This rule advises you not to withdraw more than 4% of your retirement savings per year in order to fund your retirement for at least 30 years.

